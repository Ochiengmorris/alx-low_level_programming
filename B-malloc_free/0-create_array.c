#include "main.h"
#include <stdlib.h>

/**
 * create_array - creates an array of chars.
 * @size: size of the array.
 * @c: stored char
 * Return: pointer of an array of chars
 */

char *create_array(unsigned int size, char c)
{
	char *cd;
	unsigned int i;
	if (size == 0)
	{
		return NULL;
	}
	if (cd == NULL)
	{
		return NULL;
	}
	for (i = 0; i < size; i++)
	{
		cd[i] = c;

		return NULL;
	}
}
